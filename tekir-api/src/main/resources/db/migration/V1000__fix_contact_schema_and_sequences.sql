-- Fix sequence table: rename hibernate_sequences → TEKIR_SEQUENCE if needed
DO $$
BEGIN
    IF EXISTS (SELECT 1 FROM information_schema.tables WHERE table_name = 'hibernate_sequences')
       AND NOT EXISTS (SELECT 1 FROM information_schema.tables WHERE table_name = 'tekir_sequence') THEN
        ALTER TABLE hibernate_sequences RENAME TO TEKIR_SEQUENCE;
        ALTER TABLE TEKIR_SEQUENCE RENAME COLUMN sequence_name TO NAME;
        ALTER TABLE TEKIR_SEQUENCE RENAME COLUMN sequence_next_hi_value TO NEXT_VALUE;
        UPDATE TEKIR_SEQUENCE SET NAME = 'GENERIC_SEQ' WHERE NAME = 'genericSeq';
    END IF;
END $$;

CREATE TABLE IF NOT EXISTS TEKIR_SEQUENCE (
    NAME VARCHAR(255) NOT NULL,
    NEXT_VALUE BIGINT DEFAULT 1,
    PRIMARY KEY (NAME)
);
INSERT INTO TEKIR_SEQUENCE (NAME, NEXT_VALUE) VALUES ('GENERIC_SEQ', 1) ON CONFLICT DO NOTHING;

