    IF EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='contact' AND column_name='allinone')
       AND NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='contact' AND column_name='all_type') THEN
        ALTER TABLE CONTACT RENAME COLUMN ALLINONE TO ALL_TYPE;
